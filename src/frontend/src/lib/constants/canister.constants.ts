export const ONE_DAY = 86_400n;

// Used for making freezing threshold human readable
export const ONE_MONTH = 2_592_000; // 30 days
export const THREE_MONTHS = ONE_MONTH * 3; // 7_776_000 seconds
export const SIX_MONTHS = ONE_MONTH * 6; // 15_552_000 seconds
export const ONE_YEAR = ONE_MONTH * 12; // 31_104_000 seconds, 360 days
export const TWO_YEARS = ONE_YEAR * 2;
export const FIVE_YEARS = ONE_YEAR * 5;
